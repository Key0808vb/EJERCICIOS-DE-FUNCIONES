// Función de flecha que calcula el área de un triángulo

const areaTriangulo = (base, altura) => (base * altura) / 2;

// Ejemplo:
console.log("El área de un triángulo de base 10 y altura 5 es:", areaTriangulo(10, 5)); // Salida: 25
console.log("El área del triángulo con base 8 y altura 5 es:", areaTriangulo(8, 5)); // Salida: 20
console.log("El área del triángulo con base 7 y altura 3 es:", areaTriangulo(7, 3)); // Salida: 10.5
