// Función de flecha que calcula el promedio de tres números

const promedio = (a, b, c) => (a + b + c) / 3;

console.log("El promedio de 4, 8 y 10 es:", promedio(4, 8, 10)); // Salida: 7.33
console.log("El promedio de 5, 10 y 15 es:", promedio(5, 10, 15)); // Salida: 10
console.log("El promedio de 2, 4 y 6 es:", promedio(2, 4, 6)); // Salida: 4
